    updated_count = 0
    
    for item in all_knowledge:
        keyword = item["keyword"]
        
        # Check if already exists
        existing = await MemeKnowledge.find_one(MemeKnowledge.keyword == keyword)
        
        if existing:
            # Update existing entry
            existing.definition = item["definition"]
            existing.source = item["source"]
            existing.search_query = item.get("search_query")
            await existing.save()
            updated_count += 1
            print(f"✓ Updated: {keyword}")
        else:
            # Create new entry
            meme = MemeKnowledge(
                keyword=keyword,
                definition=item["definition"],
                source=item["source"],
                search_query=item.get("search_query"),
            )
            await meme.save()
            inserted_count += 1
            print(f"✓ Inserted: {keyword}")
    
    print(f"\n完成！")
    print(f"新增: {inserted_count} 条")
    print(f"更新: {updated_count} 条")
    print(f"总计: {len(all_knowledge)} 条")


